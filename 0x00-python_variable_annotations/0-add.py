#!/usr/bin/env python3
"""Write a type-annotated function add that takes a float a
and a float b as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """Adds two numbers using type annotation
    Keyword arguments:
    a -- float variable
    b -- float variable
    Return: sum of a and b as float
    """
    return a + b